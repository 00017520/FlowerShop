//it's function to generate flower cards
function generateFlowerCards(data) {
  const section = document.getElementById("flowerCards");
  data.forEach(({ category, articles }) => {
    const categoryDiv = document.createElement("div");
    categoryDiv.className = category;

    articles.forEach(({ img, alt, caption, description }) => {
      const article = document.createElement("article");
      article.innerHTML = `
      <figure>
        <img src="${img}" alt="${alt}" />
        <figcaption>${caption}</figcaption>
      </figure>
      <p>${description}</p>
    `;
      categoryDiv.appendChild(article);
    });

    section.appendChild(categoryDiv);
  });
}

generateFlowerCards(flowerData);
